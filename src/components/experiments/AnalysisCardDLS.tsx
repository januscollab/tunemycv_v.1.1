import React, { useState } from 'react';
import { Calendar, Building, Eye, Download, Trash2, Plus } from 'lucide-react';
import { Badge } from '@/components/ui/badge';
import { Button } from '@/components/ui/button';
import { cn } from '@/lib/utils';

interface AnalysisResult {
  id: string;
  job_title: string;
  company_name: string;
  compatibility_score: number;
  created_at: string;
  executive_summary: string;
  strengths: string[];
  weaknesses: string[];
  recommendations: string[];
  credit_cost?: number;
  has_cover_letter?: boolean;
}

interface AnalysisCardDLSProps {
  analysis: AnalysisResult;
  onViewDetails: (analysis: AnalysisResult) => void;
  onDelete: (analysisId: string) => void;
  onCreateCoverLetter: (analysis: AnalysisResult) => void;
  onInterviewPrep: (analysis: AnalysisResult) => void;
  onDownload?: (analysis: AnalysisResult) => void;
  className?: string;
}

const AnalysisCardDLS: React.FC<AnalysisCardDLSProps> = ({
  analysis,
  onViewDetails,
  onDelete,
  onCreateCoverLetter,
  onInterviewPrep,
  onDownload,
  className
}) => {
  const [isHovered, setIsHovered] = useState(false);

  const handleCoverLetter = (e: React.MouseEvent) => {
    e.stopPropagation();
    onCreateCoverLetter(analysis);
  };

  const handleInterviewPrep = (e: React.MouseEvent) => {
    e.stopPropagation();
    onInterviewPrep(analysis);
  };

  const handleView = (e: React.MouseEvent) => {
    e.stopPropagation();
    onViewDetails(analysis);
  };

  const handleDownload = (e: React.MouseEvent) => {
    e.stopPropagation();
    if (onDownload) {
      onDownload(analysis);
    } else {
      // Default download logic
      const analysisContent = `
CV ANALYSIS REPORT
==================

Job Title: ${analysis.job_title || 'Untitled Position'}
Company: ${analysis.company_name || 'Company not specified'}
Compatibility Score: ${analysis.compatibility_score}%
Date: ${new Date(analysis.created_at).toLocaleDateString()}

EXECUTIVE SUMMARY
================
${analysis.executive_summary}

STRENGTHS
=========
${analysis.strengths.map((strength, index) => `${index + 1}. ${strength}`).join('\n')}

AREAS FOR IMPROVEMENT
====================
${analysis.weaknesses.map((weakness, index) => `${index + 1}. ${weakness}`).join('\n')}

RECOMMENDATIONS
===============
${analysis.recommendations.map((rec, index) => `${index + 1}. ${rec}`).join('\n')}

Generated by TuneMyCV
      `.trim();

      const blob = new Blob([analysisContent], { type: 'text/plain' });
      const url = URL.createObjectURL(blob);
      const link = document.createElement('a');
      link.href = url;
      link.download = `CV_Analysis_${analysis.job_title?.replace(/[^a-zA-Z0-9\s]/g, '_') || 'Report'}_${new Date().toISOString().split('T')[0]}.txt`;
      document.body.appendChild(link);
      link.click();
      document.body.removeChild(link);
      URL.revokeObjectURL(url);
    }
  };

  const handleDelete = (e: React.MouseEvent) => {
    e.stopPropagation();
    onDelete(analysis.id);
  };

  const formatDate = (dateString: string) => {
    return new Date(dateString).toLocaleDateString('en-US', {
      year: 'numeric',
      month: 'long',
      day: 'numeric'
    });
  };

  const formatTime = (dateString: string) => {
    return new Date(dateString).toLocaleTimeString('en-US', {
      hour: '2-digit',
      minute: '2-digit',
      hour12: true
    });
  };

  return (
    <div
      className={cn(
        "bg-white dark:bg-surface border rounded-lg p-6 cursor-pointer transition-all duration-200",
        "border-apple-core/20 dark:border-citrus/20",
        "hover:border-zapier-orange hover:shadow-md",
        className
      )}
      onMouseEnter={() => setIsHovered(true)}
      onMouseLeave={() => setIsHovered(false)}
      onClick={() => onViewDetails(analysis)}
    >
      {/* Header with date and time */}
      <div className="flex items-center text-caption text-blueberry/60 dark:text-apple-core/60 mb-4">
        <Calendar className="h-3 w-3 mr-1" />
        <span>{formatDate(analysis.created_at)} , {formatTime(analysis.created_at)}</span>
      </div>

      {/* Main content */}
      <div className="flex justify-between items-start mb-4">
        <div className="flex-1">
          <div className="flex items-center space-x-3 mb-2">
            {/* Job Title */}
            <h3 className="text-heading font-medium text-blueberry dark:text-citrus">
              {analysis.job_title || 'Untitled Position'}
            </h3>
            
            {/* CV Analysis Badge */}
            <Badge 
              variant="secondary" 
              className="text-tiny bg-muted text-blueberry dark:text-citrus"
            >
              cv analysis
            </Badge>
          </div>
          
          {/* Company */}
          <div className="flex items-center text-caption text-blueberry/60 dark:text-apple-core/60 mb-3">
            <Building className="h-3 w-3 mr-1" />
            <span>{analysis.company_name || 'Company not specified'}</span>
          </div>
        </div>
        
        {/* Compatibility Score */}
        <div className="text-right">
          <div className="text-4xl font-bold text-zapier-orange">
            {analysis.compatibility_score}%
          </div>
        </div>
      </div>

      {/* Action buttons - hidden by default, shown on hover */}
      <div 
        className={cn(
          "flex items-center justify-between transition-all duration-200",
          isHovered ? "opacity-100 translate-y-0" : "opacity-0 translate-y-2 pointer-events-none"
        )}
      >
        {/* Primary actions on the left */}
        <div className="flex items-center space-x-4">
          <Button
            variant="ghost"
            size="sm"
            onClick={handleCoverLetter}
            className="text-caption font-medium text-blueberry dark:text-citrus hover:text-zapier-orange transition-colors h-8 px-3 flex items-center space-x-2"
          >
            <Plus className="h-4 w-4" />
            <span>Generate Cover Letter</span>
          </Button>
          
          <Button
            variant="ghost"
            size="sm"
            onClick={handleInterviewPrep}
            className="text-caption font-medium text-blueberry dark:text-citrus hover:text-zapier-orange transition-colors h-8 px-3 flex items-center space-x-2"
          >
            <Plus className="h-4 w-4" />
            <span>Create Interview Prep</span>
          </Button>
        </div>

        {/* Secondary actions on the right */}
        <div className="flex items-center space-x-2">
          <Button
            variant="ghost"
            size="sm"
            onClick={handleView}
            className="text-blueberry/60 dark:text-apple-core/60 hover:text-zapier-orange transition-colors h-8 w-8 p-0"
            title="View details"
          >
            <Eye className="h-4 w-4" />
          </Button>
          
          <Button
            variant="ghost"
            size="sm"
            onClick={handleDownload}
            className="text-blueberry/60 dark:text-apple-core/60 hover:text-zapier-orange transition-colors h-8 w-8 p-0"
            title="Download"
          >
            <Download className="h-4 w-4" />
          </Button>
          
          <Button
            variant="ghost"
            size="sm"
            onClick={handleDelete}
            className="text-destructive hover:text-destructive/80 transition-colors h-8 w-8 p-0"
            title="Delete"
          >
            <Trash2 className="h-4 w-4" />
          </Button>
        </div>
      </div>
    </div>
  );
};

export default AnalysisCardDLS;